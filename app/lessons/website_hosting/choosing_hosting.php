                <div class="ml-3">
                    <h4 class="text-sm font-medium text-blue-800 dark:text-blue-200">Pro Tip: Create a Needs Document</h4>
                    <div class="mt-1 text-sm text-blue-700 dark:text-blue-300">
                        <p>Before shopping for hosting, create a document that outlines your specific needs, must-have features, and nice-to-have features. This will help you stay focused on what matters most for your website and avoid being swayed by marketing or unnecessary features.</p>
                    </div>
                </div>
            </div>
        </div>
