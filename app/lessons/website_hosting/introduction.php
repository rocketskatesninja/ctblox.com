        <h3 class="text-xl font-bold mb-2">What is Web Hosting?</h3>
        <p class="mb-3">At its core, web hosting is a service that allows individuals and organizations to make their websites accessible on the internet. When you build a website, all its files—HTML, CSS, images, videos, databases—need to be stored somewhere that's:</p>
        
        <ul class="list-disc pl-6 space-y-2 mb-4">
            <li>Connected to the internet 24/7</li>
            <li>Configured to serve these files when someone requests them</li>
            <li>Secure and reliable</li>
            <li>Capable of handling multiple visitors simultaneously</li>
        </ul>
        
        <p class="mb-3">Web hosting companies provide servers—powerful computers specifically designed for this purpose—along with the infrastructure, software, and technical support needed to keep your website accessible around the clock.</p>
        
