    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">The Web Hosting Ecosystem</h3>
        <p class="mb-3">To understand web hosting, it helps to see how it fits into the broader ecosystem of getting a website online:</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Domain Name</h4>
                <p class="text-gray-700 dark:text-gray-300">Your website's address (e.g., example.com). You register this through a domain registrar, which might be separate from or bundled with your hosting provider.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Web Hosting</h4>
                <p class="text-gray-700 dark:text-gray-300">The service that stores your website files and makes them accessible online. This is what this lesson focuses on.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">DNS (Domain Name System)</h4>
                <p class="text-gray-700 dark:text-gray-300">The system that connects your domain name to your hosting server's IP address, allowing users to find your website.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Website Files</h4>
                <p class="text-gray-700 dark:text-gray-300">The actual content of your website—HTML, CSS, JavaScript, images, videos, and databases—that are stored on the hosting server.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Content Management System (CMS)</h4>
                <p class="text-gray-700 dark:text-gray-300">Software like WordPress, Joomla, or Drupal that helps you create and manage your website content without needing to code everything manually.</p>
            </div>
        </div>
        
