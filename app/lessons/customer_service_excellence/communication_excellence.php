                </div>
                
                <div class="flex items-start">
                    <div class="flex-shrink-0">
                        <div class="flex items-center justify-center h-8 w-8 rounded-full bg-indigo-100 dark:bg-indigo-900 text-indigo-600 dark:text-indigo-300">5</div>
                    </div>
                    <div class="ml-4">
                        <h4 class="font-bold">Empathizing</h4>
                        <p class="text-gray-600 dark:text-gray-400">Acknowledging the customer's feelings and perspective</p>
                    </div>
                </div>
            </div>
        </div>
        
        <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
            <h4 class="font-bold mb-2">Active Listening Phrases</h4>
            <ul class="list-disc pl-6 space-y-1">
                <li>"I understand how frustrating that must be."</li>
                <li>"Let me make sure I've got this right..."</li>
                <li>"What I'm hearing is..."</li>
                <li>"Could you tell me more about...?"</li>
                <li>"That's a great question."</li>
                <li>"I appreciate you bringing this to our attention."</li>
            </ul>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Written Communication</h3>
        
        <p class="mb-3">In today's digital world, written communication is increasingly important in customer service. Whether you're sending emails, chat messages, or social media responses, these principles will help you communicate effectively:</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Clarity</h4>
                <p>Use simple, straightforward language. Avoid jargon, abbreviations, or complex sentences that might confuse the customer.</p>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Completeness</h4>
                <p>Provide all the information the customer needs. Anticipate follow-up questions and address them proactively.</p>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Conciseness</h4>
                <p>Be brief but thorough. Respect the customer's time by getting to the point while still being helpful.</p>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Correctness</h4>
                <p>Check your spelling, grammar, and facts. Errors can undermine your professionalism and credibility.</p>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Courtesy</h4>
                <p>Maintain a polite, respectful tone. Use please and thank you, and avoid language that could be perceived as condescending or impatient.</p>
            </div>
        </div>
        
        <div class="bg-yellow-50 dark:bg-yellow-900 p-4 rounded-lg">
            <h4 class="font-bold mb-2 text-yellow-800 dark:text-yellow-300">Email and Chat Best Practices</h4>
            <ul class="list-disc pl-6 space-y-1 text-yellow-700 dark:text-yellow-200">
                <li>Use a clear, specific subject line for emails</li>
                <li>Start with a greeting and the customer's name</li>
                <li>Use formatting (bold, bullets) to highlight key information</li>
                <li>Break long content into paragraphs for readability</li>
                <li>End with a clear call to action or next steps</li>
                <li>Include your name and contact information</li>
                <li>Proofread before sending</li>
            </ul>
        </div>
