    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">The LAST Model for Service Recovery</h3>
        
        <p class="mb-3">The LAST model provides a structured approach to service recovery:</p>
        
        <div class="grid grid-cols-1 md:grid-cols-2 gap-4 mb-4">
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <div class="flex items-center mb-2">
                    <div class="flex-shrink-0 h-10 w-10 rounded-full bg-purple-100 dark:bg-purple-900 flex items-center justify-center text-purple-600 dark:text-purple-300 font-bold text-lg">L</div>
                    <h4 class="ml-3 font-bold">Listen</h4>
                </div>
                <ul class="list-disc pl-6 space-y-1">
                    <li>Give the customer your full attention</li>
                    <li>Allow them to express their frustration</li>
                    <li>Don't interrupt or become defensive</li>
                    <li>Take notes to show you're taking them seriously</li>
                    <li>Ask clarifying questions to fully understand</li>
                </ul>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <div class="flex items-center mb-2">
                    <div class="flex-shrink-0 h-10 w-10 rounded-full bg-purple-100 dark:bg-purple-900 flex items-center justify-center text-purple-600 dark:text-purple-300 font-bold text-lg">A</div>
                    <h4 class="ml-3 font-bold">Apologize</h4>
                </div>
                <ul class="list-disc pl-6 space-y-1">
                    <li>Offer a sincere, specific apology</li>
                    <li>Take responsibility (don't blame others)</li>
                    <li>Acknowledge the impact on the customer</li>
                    <li>Express genuine regret</li>
                    <li>Avoid qualifying your apology ("I'm sorry, but...")</li>
                </ul>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <div class="flex items-center mb-2">
                    <div class="flex-shrink-0 h-10 w-10 rounded-full bg-purple-100 dark:bg-purple-900 flex items-center justify-center text-purple-600 dark:text-purple-300 font-bold text-lg">S</div>
                    <h4 class="ml-3 font-bold">Solve</h4>
                </div>
                <ul class="list-disc pl-6 space-y-1">
                    <li>Fix the immediate problem</li>
                    <li>Offer solutions, not excuses</li>
                    <li>Be creative in finding resolutions</li>
                    <li>Involve the customer in the solution</li>
                    <li>Follow through on promises</li>
                </ul>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <div class="flex items-center mb-2">
                    <div class="flex-shrink-0 h-10 w-10 rounded-full bg-purple-100 dark:bg-purple-900 flex items-center justify-center text-purple-600 dark:text-purple-300 font-bold text-lg">T</div>
                    <h4 class="ml-3 font-bold">Thank</h4>
                </div>
                <ul class="list-disc pl-6 space-y-1">
                    <li>Thank the customer for bringing the issue to your attention</li>
                    <li>Express appreciation for their patience</li>
                    <li>Acknowledge their role in helping you improve</li>
                    <li>Follow up to ensure satisfaction</li>
                    <li>Look for ways to exceed expectations</li>
                </ul>
            </div>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">The Art of the Apology</h3>
        
        <p class="mb-3">A proper apology is crucial to service recovery. Here's how to apologize effectively:</p>
        
        <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow mb-4">
            <div class="space-y-4">
                <div>
                    <h4 class="font-bold mb-2">Elements of an Effective Apology</h4>
                    <ol class="list-decimal pl-6 space-y-2">
                        <li>
                            <strong>Express regret</strong>
                            <p class="text-sm text-gray-600 dark:text-gray-400">Clearly say "I'm sorry" or "We apologize"</p>
                        </li>
                        <li>
                            <strong>Accept responsibility</strong>
                            <p class="text-sm text-gray-600 dark:text-gray-400">Acknowledge your organization's role in the problem</p>
                        </li>
                        <li>
                            <strong>Make amends</strong>
                            <p class="text-sm text-gray-600 dark:text-gray-400">Explain how you'll fix the problem and make it right</p>
                        </li>
                        <li>
                            <strong>Promise it won't happen again</strong>
                            <p class="text-sm text-gray-600 dark:text-gray-400">Explain steps taken to prevent recurrence</p>
                        </li>
                        <li>
                            <strong>Request forgiveness</strong>
                            <p class="text-sm text-gray-600 dark:text-gray-400">Ask for another chance to serve them</p>
                        </li>
                    </ol>
                </div>
                
                <div class="grid grid-cols-1 md:grid-cols-2 gap-4">
                    <div class="bg-red-50 dark:bg-red-900 p-3 rounded">
                        <h5 class="font-bold mb-1 text-red-800 dark:text-red-300">Ineffective Apology</h5>
                        <p class="text-red-700 dark:text-red-200">"I'm sorry if you were inconvenienced by the delay. These things happen sometimes, and it was a very busy day. Next time you might want to call ahead to check if we're running on schedule."</p>
                    </div>
                    <div class="bg-green-50 dark:bg-green-900 p-3 rounded">
                        <h5 class="font-bold mb-1 text-green-800 dark:text-green-300">Effective Apology</h5>
                        <p class="text-green-700 dark:text-green-200">"I'm truly sorry about the delay you experienced today. We should have communicated better about the wait time. I've already spoken with our team about improving our scheduling process, and I'd like to offer you priority booking for your next appointment to make up for today's inconvenience."</p>
                    </div>
                </div>
            </div>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Service Recovery Tools</h3>
        
        <p class="mb-3">Having the right tools at your disposal can help you recover from service failures more effectively:</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Empowerment</h4>
                <p class="mb-2">Employees need appropriate authority to resolve issues without escalation. This might include:</p>
                <ul class="list-disc pl-6 space-y-1">
                    <li>Ability to offer refunds up to a certain amount</li>
                    <li>Flexibility to make exceptions to policies</li>
                    <li>Authority to provide compensation for inconvenience</li>
                    <li>Access to resources needed to solve problems</li>
                </ul>
                <div class="mt-2 p-3 bg-blue-50 dark:bg-blue-900 rounded">
                    <p class="text-sm text-blue-800 dark:text-blue-200">The Ritz-Carlton famously empowers every employee to spend up to $2,000 per guest to resolve any issue—without requiring manager approval.</p>
                </div>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
