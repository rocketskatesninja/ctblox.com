    <h2 class="text-2xl font-bold mb-4">Chapter 5: Service Recovery</h2>
    
    <div class="mb-6">
        <p class="mb-3">Service recovery is the process of turning a negative customer experience into a positive one. Even the best organizations occasionally make mistakes or fail to meet customer expectations. How you respond to these situations often has a greater impact on customer loyalty than if everything had gone perfectly from the start.</p>
        
        <p class="mb-3">In this chapter, we'll explore the principles of effective service recovery and provide practical strategies for transforming service failures into opportunities to strengthen customer relationships.</p>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">The Service Recovery Paradox</h3>
        
        <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow mb-4">
            <p class="mb-3">The service recovery paradox is a phenomenon where customers who experience a service failure that is effectively resolved can become more loyal than customers who never experienced a problem at all.</p>
            
