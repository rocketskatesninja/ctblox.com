    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Handling Different Types of Difficult Situations</h3>
        
        <div class="space-y-4 mb-4">
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Angry Customers</h4>
                <div class="space-y-2">
                    <p><strong>Do:</strong></p>
                    <ul class="list-disc pl-6 space-y-1">
                        <li>Stay calm and maintain a neutral tone</li>
                        <li>Listen fully before responding</li>
                        <li>Acknowledge their feelings</li>
                        <li>Use the customer's name</li>
                        <li>Focus on solutions</li>
                    </ul>
                    
                    <p><strong>Don't:</strong></p>
                    <ul class="list-disc pl-6 space-y-1">
                        <li>Take it personally</li>
                        <li>Interrupt or argue</li>
                        <li>Tell them to "calm down"</li>
                        <li>Make excuses</li>
                        <li>Match their emotional intensity</li>
                    </ul>
                </div>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">When You Can't Say Yes</h4>
                <p class="mb-2">Sometimes you simply cannot fulfill a customer's request due to policy, legal restrictions, or practical limitations. In these cases:</p>
                
                <ol class="list-decimal pl-6 space-y-2">
                    <li>
                        <strong>Acknowledge the request</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">"I understand you'd like a full refund on the item you purchased six months ago."</p>
                    </li>
                    <li>
                        <strong>Express empathy</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">"I can see why this is important to you, and I wish I could help with that."</p>
                    </li>
                    <li>
                        <strong>Explain why not (briefly)</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">"Our return policy allows returns within 30 days, and this helps us maintain our quality standards."</p>
                    </li>
                    <li>
                        <strong>Offer alternatives</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">"While I can't offer a refund, I can provide store credit or arrange for a repair under our warranty program."</p>
                    </li>
                    <li>
                        <strong>End positively</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">"I'm committed to finding a solution that works for you. Would either of these options help address your concern?"</p>
                    </li>
                </ol>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Social Media Complaints</h4>
                <p class="mb-2">Public complaints require special handling:</p>
                
                <ul class="list-disc pl-6 space-y-1">
                    <li>Respond quickly (ideally within 1 hour)</li>
                    <li>Acknowledge the issue publicly</li>
                    <li>Move the conversation to a private channel</li>
                    <li>Resolve the issue thoroughly</li>
                    <li>Follow up publicly if appropriate</li>
                    <li>Document the resolution for future reference</li>
                </ul>
                
                <div class="mt-2 p-3 bg-yellow-50 dark:bg-yellow-900 rounded">
                    <p class="text-sm text-yellow-800 dark:text-yellow-200">Remember: On social media, you're not just resolving an issue for one customer—you're demonstrating your service recovery approach to everyone watching.</p>
                </div>
            </div>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Learning from Service Failures</h3>
        
        <p class="mb-3">Effective service recovery doesn't end with resolving individual issues. To truly excel, organizations must learn from failures and make systemic improvements:</p>
        
        <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow mb-4">
            <div class="space-y-4">
                <div>
                    <h4 class="font-bold mb-2">The Service Recovery Loop</h4>
                    <ol class="list-decimal pl-6 space-y-1">
                        <li>Identify service failures</li>
                        <li>Resolve individual issues</li>
                        <li>Analyze patterns and root causes</li>
                        <li>Implement systemic improvements</li>
                        <li>Monitor results</li>
                    </ol>
                </div>
                
                <div>
                    <h4 class="font-bold mb-2">Tools for Learning</h4>
                    <ul class="list-disc pl-6 space-y-1">
                        <li>Customer complaint tracking systems</li>
                        <li>Root cause analysis</li>
                        <li>Regular service recovery reviews</li>
                        <li>Post-resolution customer feedback</li>
                        <li>Employee input on recurring issues</li>
                    </ul>
                </div>
                
                <div class="p-3 bg-green-50 dark:bg-green-900 rounded">
                    <p class="font-medium text-green-800 dark:text-green-300">Questions to Ask:</p>
                    <ul class="list-disc pl-6 space-y-1 text-green-700 dark:text-green-200">
                        <li>Is this an isolated incident or part of a pattern?</li>
                        <li>What systems or processes contributed to the failure?</li>
                        <li>How could we have prevented this issue?</li>
                        <li>What early warning signs might we have missed?</li>
                        <li>How can we turn this insight into an improvement?</li>
                    </ul>
                </div>
