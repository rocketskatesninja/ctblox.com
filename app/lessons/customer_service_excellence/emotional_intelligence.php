            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">Practicing Empathy</h4>
                <p class="mb-2">Empathy is the ability to understand and share the feelings of another person. It's different from sympathy, which is feeling pity or sorrow for someone else's misfortune.</p>
                
                <div class="grid grid-cols-1 md:grid-cols-2 gap-4 mb-2">
                    <div class="bg-gray-50 dark:bg-gray-700 p-3 rounded">
                        <p class="font-medium mb-1">Sympathy:</p>
                        <p>"I'm sorry you're having this problem."</p>
                        <p class="text-sm text-gray-600 dark:text-gray-400 mt-1">Acknowledges the customer's situation but maintains distance</p>
                    </div>
                    <div class="bg-gray-50 dark:bg-gray-700 p-3 rounded">
                        <p class="font-medium mb-1">Empathy:</p>
                        <p>"I understand how frustrating this must be for you. I'd feel the same way if I were in your position."</p>
                        <p class="text-sm text-gray-600 dark:text-gray-400 mt-1">Shows understanding of the customer's perspective and emotions</p>
                    </div>
                </div>
                
                <p class="mt-2">To practice empathy in customer service:</p>
                <ul class="list-disc pl-6 space-y-1">
                    <li>Put yourself in the customer's shoes</li>
                    <li>Listen without judgment</li>
                    <li>Validate their feelings</li>
                    <li>Show genuine concern</li>
                    <li>Respond in a way that shows you understand their perspective</li>
                </ul>
            </div>
        </div>
    </div>
    
