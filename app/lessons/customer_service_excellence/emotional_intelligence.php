            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">De-escalation Techniques</h4>
                <p class="mb-2">When emotions run high, these techniques can help calm the situation:</p>
                
                <ol class="list-decimal pl-6 space-y-2">
                    <li>
                        <strong>Lower your voice and slow your speech</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">Speaking softly and slowly has a calming effect and often causes the other person to match your tone.</p>
                    </li>
                    <li>
                        <strong>Acknowledge emotions first</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">"I can hear how upset you are about this" validates feelings before moving to problem-solving.</p>
                    </li>
                    <li>
                        <strong>Use the customer's name</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">This personalizes the interaction and helps regain the customer's attention.</p>
                    </li>
                    <li>
                        <strong>Avoid defensive language</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">Replace "but" with "and" to avoid sounding dismissive of the customer's concerns.</p>
                    </li>
                    <li>
                        <strong>Give the customer control</strong>
                        <p class="text-sm text-gray-600 dark:text-gray-400">Offer choices and ask for their input on possible solutions.</p>
                    </li>
                </ol>
