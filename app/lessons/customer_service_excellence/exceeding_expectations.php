        <h3 class="text-xl font-bold mb-2">Balancing Expectations and Resources</h3>
        
        <p class="mb-3">While exceeding expectations is valuable, it's important to do so in a sustainable way:</p>
        
        <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow mb-4">
            <div class="space-y-3">
                <div>
                    <h4 class="font-bold mb-1">Be Strategic</h4>
                    <p>Focus your "wow" efforts where they'll have the most impact, such as:</p>
                    <ul class="list-disc pl-6 space-y-1">
                        <li>Key moments in the customer journey</li>
                        <li>High-value or loyal customers</li>
                        <li>Service recovery situations</li>
                        <li>Areas where you can differentiate from competitors</li>
                    </ul>
                </div>
                
                <div>
                    <h4 class="font-bold mb-1">Manage the Expectation Cycle</h4>
                    <p>Be aware that today's delighter becomes tomorrow's expectation. Continuously innovate and find new ways to surprise customers.</p>
                </div>
                
                <div>
                    <h4 class="font-bold mb-1">Empower Your Team</h4>
                    <p>Give employees the authority, resources, and recognition they need to exceed customer expectations.</p>
