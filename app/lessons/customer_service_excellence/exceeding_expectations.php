            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">4. Anticipate Needs</h4>
                <p class="mb-2">Think ahead to what customers might need before they have to ask:</p>
                <ul class="list-disc pl-6 space-y-1">
                    <li>Provide information that answers likely follow-up questions</li>
                    <li>Offer solutions to potential problems before they arise</li>
                    <li>Prepare alternatives in case the first option doesn't work</li>
                    <li>Consider the customer's next steps and make them easier</li>
                </ul>
                <div class="mt-2 p-3 bg-yellow-50 dark:bg-yellow-900 rounded">
                    <p class="text-sm text-yellow-800 dark:text-yellow-200">Example: A software company that provides video tutorials for features that customers typically struggle with, before they need to contact support.</p>
                </div>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold mb-2">5. Create Memorable Moments</h4>
                <p class="mb-2">Look for opportunities to create positive memories through:</p>
                <div class="grid grid-cols-1 md:grid-cols-2 gap-4">
                    <div>
                        <h5 class="font-medium mb-1">Peak Moments</h5>
                        <p class="text-sm text-gray-600 dark:text-gray-400">Create high points in the customer journey that stand out and leave a lasting impression.</p>
                    </div>
                    <div>
                        <h5 class="font-medium mb-1">Transition Points</h5>
                        <p class="text-sm text-gray-600 dark:text-gray-400">Pay special attention to beginnings and endings, as these are most remembered.</p>
                    </div>
                    <div>
                        <h5 class="font-medium mb-1">Recovery Moments</h5>
                        <p class="text-sm text-gray-600 dark:text-gray-400">Turn service failures into opportunities to demonstrate your commitment.</p>
                    </div>
                    <div>
                        <h5 class="font-medium mb-1">Milestone Recognition</h5>
                        <p class="text-sm text-gray-600 dark:text-gray-400">Acknowledge and celebrate customer milestones and achievements.</p>
                    </div>
                </div>
            </div>
        </div>
