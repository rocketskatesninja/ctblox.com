        <h3 class="text-xl font-bold mb-2">Registered Agent Platform (RAP)</h3>
        <p class="mb-3">Our Registered Agent Platform manages the receipt, processing, and delivery of legal documents and service of process across all jurisdictions.</p>
        
        <div class="grid grid-cols-1 md:grid-cols-2 gap-6 my-6">
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold text-indigo-600 dark:text-indigo-400 mb-2">Document Processing Workflow</h4>
                <ol class="list-decimal pl-6 space-y-2 text-sm text-gray-700 dark:text-gray-300">
                    <li><strong>Receipt:</strong> Physical or electronic document is received at our registered office</li>
                    <li><strong>Scanning:</strong> Document is digitized with OCR technology for searchability</li>
                    <li><strong>Classification:</strong> AI-assisted categorization of document type and urgency</li>
                    <li><strong>Notification:</strong> Client is alerted based on document priority</li>
                    <li><strong>Delivery:</strong> Document is securely delivered to client via portal</li>
                    <li><strong>Archiving:</strong> Document is stored in secure, searchable archive</li>
                </ol>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold text-indigo-600 dark:text-indigo-400 mb-2">RAP Security Features</h4>
                <ul class="list-disc pl-5 space-y-1 text-sm text-gray-700 dark:text-gray-300">
                    <li><strong>End-to-End Encryption:</strong> All documents are encrypted in transit and at rest</li>
                    <li><strong>Access Controls:</strong> Role-based permissions for document access</li>
                    <li><strong>Audit Trails:</strong> Comprehensive logging of all document activities</li>
                    <li><strong>Secure Facilities:</strong> Physical security at all registered office locations</li>
                    <li><strong>Backup Systems:</strong> Redundant storage and processing capabilities</li>
