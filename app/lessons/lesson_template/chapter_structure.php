    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Key Elements of a Chapter</h3>
        <p class="mb-3">Every chapter should include the following elements:</p>
        
        <div class="grid grid-cols-1 md:grid-cols-2 gap-4 my-6">
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold text-lg text-indigo-600 dark:text-indigo-400 mb-2">Clear Heading</h4>
                <p>Each chapter should start with a descriptive heading that clearly indicates the topic. Use the h2 tag with appropriate styling.</p>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold text-lg text-indigo-600 dark:text-indigo-400 mb-2">Introduction</h4>
                <p>Begin with a brief introduction that sets the context and explains what the learner will gain from the chapter.</p>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold text-lg text-indigo-600 dark:text-indigo-400 mb-2">Content Sections</h4>
                <p>Divide the content into logical sections with h3 subheadings. Each section should cover a specific aspect of the topic.</p>
            </div>
            
            <div class="bg-white dark:bg-gray-800 p-4 rounded-lg shadow">
                <h4 class="font-bold text-lg text-indigo-600 dark:text-indigo-400 mb-2">Visual Elements</h4>
                <p>Include relevant visual elements such as lists, tables, or callout boxes to enhance understanding and break up text.</p>
            </div>
        </div>
    </div>
    
