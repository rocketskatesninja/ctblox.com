    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Text Formatting</h3>
        <p class="mb-3">When formatting text, consider the following guidelines:</p>
        
        <ul class="list-disc pl-6 space-y-2 mb-4">
            <li><strong>Headings:</strong> Use hierarchical headings (h2 for chapter titles, h3 for sections, h4 for subsections)</li>
            <li><strong>Paragraphs:</strong> Keep paragraphs short (3-5 sentences) and focused on a single idea</li>
            <li><strong>Emphasis:</strong> Use bold for important terms and concepts</li>
            <li><strong>Spacing:</strong> Include appropriate spacing between elements for visual clarity</li>
        </ul>
        
        <p class="mb-3">Consistent formatting helps learners navigate the content and identify key information quickly.</p>
    </div>
    
