    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Creating Service Standards</h3>
        <p class="mb-3">Service standards define what quality service looks like in your business. They serve as guidelines for you and your team, ensuring consistency across all customer interactions.</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Key Areas for Service Standards</h4>
                <ul class="list-disc pl-6 space-y-1 text-gray-700 dark:text-gray-300">
                    <li><strong>Response times</strong> - How quickly will you respond to different types of customer inquiries?</li>
                    <li><strong>Communication style</strong> - What tone, language, and level of formality will you use?</li>
                    <li><strong>Problem resolution</strong> - What authority do team members have to resolve issues?</li>
                    <li><strong>Follow-up procedures</strong> - When and how will you check in with customers after interactions?</li>
                    <li><strong>Service environment</strong> - What will the physical or digital customer experience be like?</li>
                </ul>
            </div>
            
            <div class="bg-blue-50 dark:bg-blue-900 p-4 rounded-lg">
                <div class="flex items-start">
                    <div class="flex-shrink-0">
                        <svg class="h-6 w-6 text-blue-600 dark:text-blue-300" fill="currentColor" viewBox="0 0 20 20">
                            <path fill-rule="evenodd" d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7-4a1 1 0 11-2 0 1 1 0 012 0zM9 9a1 1 0 000 2v3a1 1 0 001 1h1a1 1 0 100-2v-3a1 1 0 00-1-1H9z" clip-rule="evenodd"></path>
                        </svg>
                    </div>
                    <div class="ml-3">
                        <h4 class="text-sm font-medium text-blue-800 dark:text-blue-200">Example: Email Response Standard</h4>
                        <div class="mt-1 text-sm text-blue-700 dark:text-blue-300">
                            <p>"All customer emails will receive an initial response within 4 business hours. If a complete resolution isn't possible in the first response, we'll provide an estimated timeline for resolution and follow up at least once every 24 hours until the matter is resolved."</p>
                        </div>
                    </div>
                </div>
            </div>
        </div>
        
        <p class="mb-3">When creating service standards:</p>
        <ul class="list-disc pl-6 space-y-2 mb-4">
            <li>Make them specific and measurable</li>
            <li>Ensure they're realistic and achievable</li>
            <li>Document them clearly</li>
            <li>Train your team thoroughly</li>
            <li>Review and update them regularly</li>
        </ul>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Customer Service Workflows</h3>
        <p class="mb-3">Workflows are step-by-step processes for handling common customer interactions. They ensure consistency and help team members know exactly what to do in various situations.</p>
        
        <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg mb-4">
            <h4 class="font-medium text-gray-900 dark:text-white mb-2">Sample Workflow: Handling a Product Return</h4>
            <ol class="list-decimal pl-6 space-y-1 text-gray-700 dark:text-gray-300">
                <li>Greet the customer and ask for their order information</li>
                <li>Verify the purchase in your system</li>
                <li>Ask the reason for the return and document it</li>
                <li>Check if the return meets your policy requirements</li>
                <li>If approved, provide return instructions or process the return on the spot</li>
                <li>Issue the refund or exchange according to your policy</li>
                <li>Thank the customer and ask if there's anything else they need</li>
                <li>Update your inventory system</li>
                <li>Follow up with an email confirmation of the completed return</li>
            </ol>
        </div>
        
        <p class="mb-3">Key workflows to document for your business might include:</p>
        <ul class="list-disc pl-6 space-y-2 mb-4">
            <li>New customer onboarding</li>
            <li>Order processing and fulfillment</li>
            <li>Handling returns and exchanges</li>
            <li>Responding to common questions</li>
            <li>Addressing complaints</li>
            <li>Collecting and implementing feedback</li>
        </ul>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Customer Relationship Management (CRM)</h3>
        <p class="mb-3">A CRM system helps you track customer interactions, preferences, and history. Even a simple CRM can dramatically improve your service by ensuring you have the right information at your fingertips.</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Essential CRM Information</h4>
                <ul class="list-disc pl-6 space-y-1 text-gray-700 dark:text-gray-300">
                    <li><strong>Contact details</strong> - Name, email, phone, preferred contact method</li>
                    <li><strong>Purchase history</strong> - What they've bought, when, and for how much</li>
                    <li><strong>Service interactions</strong> - Previous issues, how they were resolved</li>
                    <li><strong>Preferences</strong> - Product preferences, communication preferences</li>
                    <li><strong>Important dates</strong> - Birthdays, anniversaries, renewal dates</li>
                    <li><strong>Notes</strong> - Personal details they've shared, special circumstances</li>
                </ul>
            </div>
            
            <div class="bg-yellow-50 dark:bg-yellow-900 p-4 rounded-lg">
                <h4 class="font-medium text-yellow-800 dark:text-yellow-200">CRM Options for Small Businesses</h4>
                <p class="text-yellow-700 dark:text-yellow-300 mb-2">You don't need an expensive system to start. Options include:</p>
                <ul class="list-disc pl-6 space-y-1 text-yellow-700 dark:text-yellow-300">
                    <li><strong>Spreadsheets</strong> - Simple, free, and effective for very small businesses</li>
                    <li><strong>Email tools</strong> - Many email marketing platforms include basic CRM features</li>
                    <li><strong>Free CRM platforms</strong> - Several offer free tiers for small businesses</li>
                    <li><strong>Industry-specific solutions</strong> - Many industries have affordable CRM options tailored to their needs</li>
                </ul>
            </div>
        </div>
        
        <div class="bg-blue-50 dark:bg-blue-900 p-4 rounded-lg mb-4">
