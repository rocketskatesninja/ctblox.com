                <div class="ml-3">
                    <h4 class="text-sm font-medium text-blue-800 dark:text-blue-200">CRM Best Practice</h4>
                    <div class="mt-1 text-sm text-blue-700 dark:text-blue-300">
                        <p>Update your CRM immediately after every customer interaction. This ensures that anyone who speaks with the customer next has the most current information.</p>
                    </div>
                </div>
            </div>
