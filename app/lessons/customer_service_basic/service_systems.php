    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Feedback Systems</h3>
        <p class="mb-3">Systematic feedback collection helps you continuously improve your service. Implement these feedback mechanisms:</p>
        
        <div class="grid grid-cols-1 md:grid-cols-2 gap-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Post-Purchase Surveys</h4>
                <p class="text-gray-700 dark:text-gray-300">Send a brief survey after each purchase to gauge satisfaction. Keep it short (3-5 questions) and focus on the most important aspects of the experience.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Net Promoter Score (NPS)</h4>
                <p class="text-gray-700 dark:text-gray-300">Ask "On a scale of 0-10, how likely are you to recommend us to a friend or colleague?" This single question provides powerful insights into customer loyalty.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Follow-Up Calls</h4>
                <p class="text-gray-700 dark:text-gray-300">For high-value customers or after resolving issues, a personal call can provide deeper insights than surveys and strengthen relationships.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Social Media Monitoring</h4>
                <p class="text-gray-700 dark:text-gray-300">Monitor mentions of your business on social platforms to catch feedback that customers might not share directly with you.</p>
            </div>
        </div>
        
        <div class="bg-green-50 dark:bg-green-900 p-4 rounded-lg mb-4">
            <h4 class="font-medium text-green-800 dark:text-green-200">The Feedback Loop</h4>
            <p class="text-green-700 dark:text-green-300 mb-2">Effective feedback systems follow this loop:</p>
            <ol class="list-decimal pl-6 space-y-1 text-green-700 dark:text-green-300">
                <li><strong>Collect</strong> feedback systematically</li>
                <li><strong>Analyze</strong> to identify patterns and priorities</li>
                <li><strong>Act</strong> on the insights by making improvements</li>
                <li><strong>Communicate</strong> changes to customers ("You spoke, we listened")</li>
                <li><strong>Measure</strong> the impact of your changes</li>
            </ol>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Training and Development</h3>
        <p class="mb-3">If you have employees, their training is critical to your service quality. Even if you're a solo entrepreneur, ongoing self-development in customer service is valuable.</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Training Components</h4>
                <ul class="list-disc pl-6 space-y-1 text-gray-700 dark:text-gray-300">
                    <li><strong>Initial onboarding</strong> - Comprehensive introduction to your service standards and systems</li>
                    <li><strong>Role-playing</strong> - Practice handling common and difficult situations</li>
                    <li><strong>Shadowing</strong> - New team members observe experienced ones</li>
                    <li><strong>Regular refreshers</strong> - Brief sessions to reinforce key principles</li>
                    <li><strong>Skill-specific training</strong> - Focus on areas like communication or problem-solving</li>
                </ul>
            </div>
            
            <div class="bg-purple-50 dark:bg-purple-900 p-4 rounded-lg">
                <h4 class="font-medium text-purple-800 dark:text-purple-200">Empowerment is Key</h4>
                <p class="text-purple-700 dark:text-purple-300">Train your team to make decisions and solve problems without always escalating to management. Clear guidelines on what they can offer customers (refunds, discounts, exceptions) help them resolve issues quickly and confidently.</p>
            </div>
        </div>
