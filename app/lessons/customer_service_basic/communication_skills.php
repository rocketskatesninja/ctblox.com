    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Written Communication</h3>
        <p class="mb-3">For emails, chat, and social media interactions, written communication skills are essential:</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Email Best Practices</h4>
                <ul class="list-disc pl-6 space-y-1 text-gray-700 dark:text-gray-300">
                    <li>Use a clear, specific subject line</li>
                    <li>Start with a personalized greeting using the customer's name</li>
                    <li>Keep paragraphs short and scannable</li>
                    <li>Use bullet points for multiple items or steps</li>
                    <li>End with a clear call to action or next steps</li>
                    <li>Include your contact information</li>
                    <li>Proofread before sending</li>
                </ul>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Chat and Social Media</h4>
                <ul class="list-disc pl-6 space-y-1 text-gray-700 dark:text-gray-300">
                    <li>Respond promptly (aim for under 5 minutes for chat, under 1 hour for social media)</li>
                    <li>Use a friendly, conversational tone while maintaining professionalism</li>
                    <li>Keep messages concise but complete</li>
                    <li>Use emojis sparingly and appropriately to convey tone</li>
                    <li>Provide a clear resolution or path forward</li>
                </ul>
            </div>
        </div>
        
