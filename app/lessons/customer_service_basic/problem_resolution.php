    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Service Recovery</h3>
        <p class="mb-3">Service recovery refers to the actions you take to turn a negative customer experience into a positive one. Effective service recovery can actually create stronger customer loyalty than if no problem had occurred—a phenomenon known as the "service recovery paradox."</p>
        
        <div class="space-y-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">The Five Steps of Service Recovery</h4>
                <ol class="list-decimal pl-6 space-y-1 text-gray-700 dark:text-gray-300">
                    <li><strong>Apologize</strong> sincerely and take responsibility</li>
                    <li><strong>Review</strong> the situation with the customer to ensure understanding</li>
                    <li><strong>Fix</strong> the immediate problem</li>
                    <li><strong>Prevent</strong> similar problems in the future</li>
                    <li><strong>Compensate</strong> the customer for their inconvenience when appropriate</li>
                </ol>
            </div>
            
            <div class="bg-yellow-50 dark:bg-yellow-900 p-4 rounded-lg">
                <h4 class="font-medium text-yellow-800 dark:text-yellow-200">When to Offer Compensation</h4>
                <p class="text-yellow-700 dark:text-yellow-300 mb-2">Consider offering compensation when:</p>
                <ul class="list-disc pl-6 space-y-1 text-yellow-700 dark:text-yellow-300">
                    <li>The customer experienced a significant inconvenience</li>
                    <li>Your business made a clear error</li>
                    <li>The customer had to make multiple attempts to resolve an issue</li>
                    <li>The problem resulted in financial loss for the customer</li>
                </ul>
                <p class="text-yellow-700 dark:text-yellow-300 mt-2">Compensation doesn't always mean refunds. Consider:</p>
                <ul class="list-disc pl-6 space-y-1 text-yellow-700 dark:text-yellow-300">
                    <li>Discounts on future purchases</li>
                    <li>Free shipping or delivery</li>
                    <li>Product upgrades or add-ons</li>
                    <li>Extended warranties or service periods</li>
                    <li>Small gifts or tokens of appreciation</li>
                </ul>
            </div>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Learning from Problems</h3>
        <p class="mb-3">Every customer problem is an opportunity to improve your business. Implement these practices to learn from issues:</p>
        
        <ul class="list-disc pl-6 space-y-2 mb-4">
            <li><strong>Document all complaints</strong> in a central system</li>
            <li><strong>Look for patterns</strong> in the types of issues customers report</li>
            <li><strong>Analyze root causes</strong> rather than just addressing symptoms</li>
            <li><strong>Share learnings</strong> with your team</li>
            <li><strong>Implement process changes</strong> to prevent recurrence</li>
            <li><strong>Follow up with customers</strong> to ensure the changes are effective</li>
        </ul>
        
        <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg mb-4">
            <p class="italic text-gray-700 dark:text-gray-300">"Your most unhappy customers are your greatest source of learning." — Bill Gates</p>
        </div>
    </div>
    
