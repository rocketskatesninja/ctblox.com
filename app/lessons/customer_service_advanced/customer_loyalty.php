        <h3 class="text-xl font-bold mb-2">Putting It Into Practice</h3>
        <p class="mb-3">To implement a successful loyalty program for your small business:</p>
        
        <ol class="list-decimal pl-6 space-y-2 text-gray-700 dark:text-gray-300 mb-4">
            <li><strong>Assess your business needs</strong> and set clear objectives for your loyalty program</li>
            <li><strong>Choose a program structure</strong> that aligns with your customer behavior and business model</li>
            <li><strong>Design a simple but compelling</strong> reward structure with attainable milestones</li>
            <li><strong>Select appropriate technology</strong> that balances functionality with your budget</li>
            <li><strong>Train your team</strong> to promote and manage the program effectively</li>
            <li><strong>Launch with clear communication</strong> and initial incentives to drive enrollment</li>
            <li><strong>Measure key metrics</strong> and continuously optimize your program</li>
        </ol>
        
