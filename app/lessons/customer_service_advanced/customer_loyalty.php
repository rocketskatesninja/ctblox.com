    <div class="mb-6">
        <h2 class="text-2xl font-bold mb-3">Building Customer Loyalty Programs</h2>
        <p class="mb-3">Customer loyalty programs are structured marketing strategies designed to encourage customers to continue using your products or services. For small businesses, an effective loyalty program can significantly increase customer retention, boost revenue, and create brand advocates.</p>
        
        <p class="mb-3">This chapter explores how to design, implement, and optimize loyalty programs that drive meaningful results for your small business.</p>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">The Business Case for Loyalty Programs</h3>
        <p class="mb-3">Before investing in a loyalty program, it's important to understand the potential returns:</p>
        
        <div class="grid grid-cols-1 md:grid-cols-2 gap-4 mb-4">
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Increased Customer Retention</h4>
                <p class="text-gray-700 dark:text-gray-300">Loyalty programs can increase retention rates by 5-10%. Given that a 5% increase in retention can boost profits by 25-95%, this impact is significant.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Higher Customer Lifetime Value</h4>
                <p class="text-gray-700 dark:text-gray-300">Loyal customers spend 67% more than new ones. They also make more frequent purchases and are more likely to try new offerings.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Cost-Effective Acquisition</h4>
                <p class="text-gray-700 dark:text-gray-300">Referrals from loyal customers typically convert at 4x the rate of other leads and have a 37% higher retention rate.</p>
            </div>
            
            <div class="bg-gray-100 dark:bg-gray-800 p-4 rounded-lg">
                <h4 class="font-medium text-gray-900 dark:text-white">Valuable Customer Data</h4>
                <p class="text-gray-700 dark:text-gray-300">Loyalty programs provide insights into customer preferences and behaviors that can inform product development and marketing strategies.</p>
            </div>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Types of Loyalty Programs</h3>
