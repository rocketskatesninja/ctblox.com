        <div class="bg-purple-50 dark:bg-purple-900 p-4 rounded-lg mb-4">
            <h4 class="font-medium text-purple-800 dark:text-purple-200 mb-2">Optimization Strategies</h4>
            <ul class="list-disc pl-6 space-y-1 text-purple-700 dark:text-purple-300">
                <li><strong>A/B test rewards:</strong> Try different reward structures with different customer segments to see what drives the most engagement</li>
                <li><strong>Gather member feedback:</strong> Survey program members about what they value and what could be improved</li>
                <li><strong>Analyze redemption patterns:</strong> Identify which rewards are most popular and which are rarely redeemed</li>
                <li><strong>Review inactive members:</strong> Determine why some members stop participating and develop re-engagement strategies</li>
                <li><strong>Refresh regularly:</strong> Update your program every 12-18 months to maintain excitement and address changing customer preferences</li>
            </ul>
        </div>
    </div>
    
    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Common Pitfalls to Avoid</h3>
        <p class="mb-3">Be aware of these frequent loyalty program mistakes:</p>
        
        <div class="grid grid-cols-1 md:grid-cols-2 gap-4 mb-4">
            <div class="bg-red-50 dark:bg-red-900 p-4 rounded-lg">
                <h4 class="font-medium text-red-800 dark:text-red-200">Making Rewards Too Difficult to Earn</h4>
                <p class="text-red-700 dark:text-red-300">If customers can't reach meaningful rewards within a reasonable timeframe, they'll lose interest in your program.</p>
            </div>
            
            <div class="bg-red-50 dark:bg-red-900 p-4 rounded-lg">
                <h4 class="font-medium text-red-800 dark:text-red-200">Offering Irrelevant Rewards</h4>
                <p class="text-red-700 dark:text-red-300">Rewards that don't align with customer values or interests won't motivate participation, regardless of their monetary value.</p>
            </div>
            
            <div class="bg-red-50 dark:bg-red-900 p-4 rounded-lg">
                <h4 class="font-medium text-red-800 dark:text-red-200">Neglecting Program Communication</h4>
                <p class="text-red-700 dark:text-red-300">Without regular updates about their status and available rewards, customers will forget about your program.</p>
            </div>
            
            <div class="bg-red-50 dark:bg-red-900 p-4 rounded-lg">
                <h4 class="font-medium text-red-800 dark:text-red-200">Creating Excessive Complexity</h4>
                <p class="text-red-700 dark:text-red-300">Complicated earning rules, redemption processes, or program structures create friction and reduce participation.</p>
            </div>
        </div>
    </div>
    
