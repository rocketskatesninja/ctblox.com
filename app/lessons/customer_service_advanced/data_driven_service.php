    <div class="mb-6">
        <h3 class="text-xl font-bold mb-2">Essential Customer Service Metrics</h3>
        <p class="mb-3">To implement data-driven service, you need to measure the right things. Here are the key metrics small businesses should track:</p>
        
        <div class="overflow-hidden bg-white dark:bg-gray-800 shadow sm:rounded-lg mb-6">
            <table class="min-w-full divide-y divide-gray-200 dark:divide-gray-700">
                <thead class="bg-gray-50 dark:bg-gray-700">
                    <tr>
                        <th scope="col" class="px-6 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-300 uppercase tracking-wider">Metric</th>
                        <th scope="col" class="px-6 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-300 uppercase tracking-wider">What It Measures</th>
                        <th scope="col" class="px-6 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-300 uppercase tracking-wider">How to Calculate</th>
                        <th scope="col" class="px-6 py-3 text-left text-xs font-medium text-gray-500 dark:text-gray-300 uppercase tracking-wider">Target Range</th>
                    </tr>
                </thead>
                <tbody class="bg-white dark:bg-gray-800 divide-y divide-gray-200 dark:divide-gray-700">
                    <tr>
                        <td class="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900 dark:text-white">Net Promoter Score (NPS)</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Customer loyalty and likelihood to recommend</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">% Promoters - % Detractors</td>
