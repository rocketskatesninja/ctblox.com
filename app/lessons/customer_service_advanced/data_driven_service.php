                    <tr>
                        <td class="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900 dark:text-white">Resolution Rate</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Ability to resolve issues on first contact</td>
