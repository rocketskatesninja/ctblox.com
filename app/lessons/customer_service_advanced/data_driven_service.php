                    </tr>
                    <tr>
                        <td class="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900 dark:text-white">Customer Satisfaction (CSAT)</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Satisfaction with specific interactions</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">(# of satisfied responses ÷ total responses) × 100</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">80%+ (Good)<br>90%+ (Excellent)</td>
                    </tr>
                    <tr>
                        <td class="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900 dark:text-white">Customer Effort Score (CES)</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Ease of getting needs met</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Average of responses on a 1-7 scale</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">5.5+ (Good)<br>6.0+ (Excellent)</td>
                    </tr>
                    <tr>
                        <td class="px-6 py-4 whitespace-nowrap text-sm font-medium text-gray-900 dark:text-white">First Response Time</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Speed of initial response to inquiries</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Average time between inquiry receipt and first response</td>
                        <td class="px-6 py-4 text-sm text-gray-500 dark:text-gray-400">Varies by channel:<br>Email: <24 hours<br>Social: <60 minutes<br>Phone: <3 minutes</td>
