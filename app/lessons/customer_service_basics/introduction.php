<p>Customer service isn't just the responsibility of a dedicated support team—it's an organization-wide commitment that involves:</p>

<ul class="list-disc pl-5 mt-2 space-y-2">
    <li><span class="font-semibold">Front-line support staff</span> who directly interact with customers</li>
    <li><span class="font-semibold">Product development teams</span> who create user-friendly products</li>
    <li><span class="font-semibold">Marketing teams</span> who set appropriate customer expectations</li>
    <li><span class="font-semibold">Leadership</span> who establish customer-centric values and policies</li>
    <li><span class="font-semibold">Every employee</span> who contributes to the overall customer experience</li>
</ul>

